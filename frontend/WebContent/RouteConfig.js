 var myApp=angular.module("myApp",['ngRoute']);
myApp.config(function($routeProvider)
{
	$routeProvider.when("/",{templateUrl:"template/home.html"})
	           .when("/home",{templateUrl:"template/home.html"})
	           .when("/aboutus",{templateUrl:"template/aboutus.html"})
	           .when("/contactus",{templateUrl:"template/contactus.html"})
	           .when("/blog",{templateUrl:"c_blog/blog1.html"})
	           .when("/forum",{templateUrl:"c_forum/forum1.html"})
	           .when("/newforum",{templateUrl:"c_forum/forum.html"})
	           .when("/job",{templateUrl:"c_job/job.html"})
	           .when("/login",{templateUrl:"c_user/login.html"})
	           .when("/registration",{templateUrl:"c_user/registration.html"})
	           .when("/updateblog",{templateUrl:"c_blog/updateblog.html"})
	           .when("/updateforum",{templateUrl:"c_forum/updateforum.html"})
	           .when("/updatejob",{templateUrl:"c_job/updatejob.html"})
	           .when("/profile",{templateUrl:"c_profile/UpdateProfile.html"})
	           .when("/blogcomment",{templateUrl:"c_blog/blogcomment.html"})
	           .when("/updateblogcomment",{templateUrl:"c_blog/updateblogcomment.html"})
	           .when("/userhome",{templateUrl:"c_user/userhome.html"})
	           .when("/chat",{templateUrl:"c_chat/chat.html"})
	           .when("/newblog",{templateUrl:"c_blog/newblog.html"})
	          .when("/newblogcomment",{templateUrl:"c_blog/newbcomment.html"})
	          .when("/forumcomment",{templateUrl:"c_forum/forumcomment.html"})
});
